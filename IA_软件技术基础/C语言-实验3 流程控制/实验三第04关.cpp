#include<stdio.h>
#include<math.h>
int main()
{
	int i,x,k,flag=0,tag=0,n,num;

	scanf("%d",&n);

	//遍历10^n-1到10^n
	for(int num=((int)pow(10, n-1)); num<((int)pow(10, n)) ; num++)
	{	
		x=num;
		while(x)
		{
			flag=0;
			//判断是否为合数
			for(i=2,k=x-1; i<=k && !flag; i++)
			{	
				if(!(x%i)) flag=1;
			}
			//对非合数打上标签
			if(!flag) 
			{
				tag=1;
				break;
			}
			//去除最右一位
			else x/=10;
		}
		//判断纯粹合数
		if(tag==0) printf("%d ",num);
		tag=0;
	}

	return 0;
}


